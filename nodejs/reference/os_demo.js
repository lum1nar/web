const os = require("node:os");

// platform
console.log(os.platform());
// uptime
console.log(os.uptime());
//  CPU Arch
console.log(os.arch());
// CPU Core Info
console.log(os.cpus());
// Free Memory
console.log(os.freemem());
// Total Memory
console.log(os.totalmem());
// Home Dir
console.log(os.homedir());
